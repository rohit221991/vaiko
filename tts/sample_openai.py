from pathlib import Path
from openai import OpenAI
client = OpenAI()

text = '''
अर्जुन, एक 10 साल का लड़का था, जो अपने लेटेस्ट वीडियो गेम्स के कलेक्शन को बहुत पसंद करता था। उसके पास हर तरह के गेम्स थे, लेकिन फिर भी वह एक स्पेशल गेम के लिए अपनी पॉकेट मनी बचा रहा था।

एक दिन, स्कूल से घर आते वक्त, उसने देखा कि कुछ बच्चे सड़क पर एक पुराने फोन में गेम खेलने की कोशिश कर रहे थे। गेम बार-बार रुक रहा था और बच्चे उदास हो रहे थे।

अर्जुन के दिल में एक सोच आई। उसने सोचा, "शायद, खुशियाँ बाँटने से बढ़ती हैं।" उसने अपने सेव्ड पैसे से वो स्पेशल गेम नहीं खरीदा। इंस्टेड, उसने वो पैसे उन बच्चों को दे दिए ताकि वो एक नया फोन ले सकें और बिना रुके गेम खेल सकें।

उस रात, अर्जुन को नींद नहीं आई। उसने सोचा कि उसने अपने सपने का गेम नहीं खरीदा, लेकिन फिर भी उसको एक अजीब सी खुशी महसूस हुई।

अगली सुबह, उसका बेस्ट फ्रेंड रवि उसके घर आया और उसके हाथ में वही गेम था जो अर्जुन खरीदना चाहता था। रवि बोला, "ये गेम मैंने दो बार जीत लिया है, अब ये तेरे लिए। और सुन, मैंने सुना है कि तूने जो किया उससे सब लोग बहुत खुश हैं।"

अर्जुन समझ गया कि असली खुशियाँ तो शेयरिंग से आती हैं। उसने रवि के साथ वो गेम खेला और उन दोनों ने मिलकर नए हाई स्कोर्स बनाए
'''

speech_file_path = Path(__file__).parent / "story.mp3"
response = client.audio.speech.create(
    model="tts-1",
    voice="nova",
    input=text
)

response.stream_to_file(speech_file_path)
